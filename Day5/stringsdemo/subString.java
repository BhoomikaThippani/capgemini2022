package com.demo.stringsdemo;

public class subString {
	public static void main(String args[])
	{
		String s1="Bhoomika Varma";
		System.out.println("Original String  "+ s1);
		System.out.println("Substring starting from index  7: "+s1.substring(7));
		System.out.println("Substring starting from index 0 to 10:"+s1.substring(0,10));
		
	}

}
