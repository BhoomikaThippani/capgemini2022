package com.demo.stringsdemo;

public class contains {
	public static void main(String args[]){  
		String name="hii, Iam Bhoomika";  
		System.out.println(name.contains(", Iam"));  
		System.out.println(name.contains(" Iam Bhoomika"));  
		System.out.println(name.contains("hello"));  
		}
}


//contains() method returns true , when the mentioned Substring is present in the  Main string.